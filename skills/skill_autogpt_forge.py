# ② AgentTemplate -- 骨架模板生成器
# ═══════════════════════════════════════════════════

class AgentTemplate:
    """Agent骨架模板生成器 -- 类似create-react-app的scaffold"""
    
    @staticmethod
    def _base_template(name, class_name, description):
        lines = []
        lines.append('"""')
        lines.append(f"{name} -- AutoGPT Agent (Generated by Forge)")
        lines.append('"""')
        lines.append("from typing import Dict, Any, Optional")
        lines.append("import json")
        lines.append("")
        lines.append(f"class {class_name}:")
        lines.append('    """' + description + '"""')
        lines.append("    ")
        lines.append('    def __init__(self, config: Optional[Dict[str, Any]] = None):')
        lines.append("        self.config = config or {}")
        lines.append(f'        self.name = "{name}"')
        lines.append("        self.memory: Dict[str, Any] = {}")
        lines.append("        ")
        lines.append('    def run(self, task: str) -> str:')
        lines.append('        """execute task"""')
        lines.append('        return f"{self.name} processed: {task}"')
        lines.append("    ")
        lines.append('    def save_state(self, path: str) -> None:')
        lines.append('        with open(path, "w") as f:')
        lines.append('            json.dump({"name": self.name, "memory": self.memory}, f)')
        lines.append("    ")
        lines.append('    @classmethod')
        lines.append(f'    def load_state(cls, path: str) -> "{class_name}":')
        lines.append("        with open(path) as f:")
        lines.append("            data = json.load(f)")
        lines.append("        agent = cls()")
        lines.append('        agent.memory = data.get("memory", {})')
        lines.append("        return agent")
        return "\n".join(lines)
    
    @classmethod
    def scaffold(cls, name: str, output_dir: str, 
                 description: str = "AutoGPT Agent",
                 include_tests: bool = True) -> str:
        """生成Agent骨架"""
        class_name = "".join(word.capitalize() for word in name.replace("-", "_").split("_"))
        
        # 生成主文件
        code = cls._base_template(name, class_name, description)
        
        os.makedirs(output_dir, exist_ok=True)
        main_path = os.path.join(output_dir, f"{name}.py")
        with open(main_path, "w") as f:
            f.write(code)
        
        # 生成配置文件
        config = AgentConfig(name=name, description=description)
        config_path = os.path.join(output_dir, f"{name}_config.yaml")
        with open(config_path, "w") as f:
            f.write(config.to_yaml())
        
        # 生成测试骨架
        if include_tests:
            test_lines = []
            test_lines.append('"""')
            test_lines.append(f"Tests for {name}")
            test_lines.append('"""')
            test_lines.append(f"from {name} import {class_name}")
            test_lines.append("")
            test_lines.append(f"def test_{name}_basic():")
            test_lines.append(f"    agent = {class_name}()")
            test_lines.append('    result = agent.run("hello")')
            test_lines.append('    assert "processed" in result')
            test_lines.append("")
            test_lines.append(f"def test_{name}_save_load(tmpdir):")
            test_lines.append(f"    agent = {class_name}()")
            test_lines.append('    path = tmpdir / "state.json"')
            test_lines.append("    agent.save_state(str(path))")
            test_lines.append("    assert path.exists()")
            test_lines.append(f"    loaded = {class_name}.load_state(str(path))")
            test_lines.append("    assert loaded.name == agent.name")
            test_code = "\n".join(test_lines)
            
            test_dir = os.path.join(output_dir, "tests")
            os.makedirs(test_dir, exist_ok=True)
            test_path = os.path.join(test_dir, f"test_{name}.py")
            with open(test_path, "w") as f:
                f.write(test_code)
        
        return main_path


