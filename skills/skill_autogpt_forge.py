# ⑤ ConfigValidator -- 配置校验
# ═══════════════════════════════════════════════════

@dataclass
class ValidationError:
    field: str
    message: str
    severity: str = "error"  # error / warning / info

class ConfigValidator:
    """配置校验器"""
    
    RULES = {
        "name": lambda v: (len(v) > 0, "Name cannot be empty"),
        "max_iterations": lambda v: (1 <= v <= 10000, "max_iterations must be 1-10000"),
        "timeout": lambda v: (v > 0, "timeout must be positive"),
        "max_concurrency": lambda v: (v >= 1, "max_concurrency must be >= 1"),
    }
    
    @classmethod
    def validate(cls, config: AgentConfig) -> List[ValidationError]:
        errors = []
        for field, rule in cls.RULES.items():
            if hasattr(config, field):
                ok, msg = rule(getattr(config, field))
                if not ok:
                    errors.append(ValidationError(field=field, message=msg))
        return errors
    
    @classmethod
    def validate_yaml(cls, path: str) -> Tuple[bool, List[ValidationError]]:
        try:
            config = AgentConfig.from_yaml(path)
            errors = cls.validate(config)
            return len(errors) == 0, errors
        except Exception as e:
            return False, [ValidationError(field="file", message=str(e))]


