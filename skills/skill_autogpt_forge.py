# ④ AgentBuilder -- Builder模式
# ═══════════════════════════════════════════════════

class AgentBuilder:
    """Builder模式构建Agent -- 链式调用"""
    
    def __init__(self, name: str = "GA_Agent"):
        self._config = AgentConfig(name=name)
        self._components: Dict[str, Any] = {}
        self._plugins: List[Any] = []
    
    def with_llm(self, provider: str = "openai", model: str = "gpt-4",
                 **kwargs) -> "AgentBuilder":
        self._config.llm = LLMConfig(provider=provider, model=model, **kwargs)
        return self
    
    def with_tool(self, tool_name: str) -> "AgentBuilder":
        self._config.tools.append(tool_name)
        return self
    
    def with_memory(self, memory_type: str = "basic", **kwargs) -> "AgentBuilder":
        self._config.memory = {"type": memory_type, **kwargs}
        return self
    
    def with_plugin(self, plugin_path: str) -> "AgentBuilder":
        self._config.plugins.append(plugin_path)
        return self
    
    def with_system_prompt(self, prompt: str) -> "AgentBuilder":
        self._config.system_prompt = prompt
        return self
    
    def with_config(self, config: AgentConfig) -> "AgentBuilder":
        self._config = config
        return self
    
    def build(self) -> Dict[str, Any]:
        """构建完成,返回配置和组件快照"""
        return {
            "config": self._config,
            "components": self._components,
            "plugins": self._plugins,
            "profile": AgentProfile(
                name=self._config.name,
                version=self._config.version,
                capabilities=self._config.tools + list(self._components.keys()),
                config_hash=hashlib.md5(
                    json.dumps(self._config.to_dict(), sort_keys=True).encode()
                ).hexdigest()[:12]
            )
        }


