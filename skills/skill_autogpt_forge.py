# ⑥ PluginSystem -- 插件系统
# ═══════════════════════════════════════════════════

class PluginBase:
    """插件基类"""
    name: str = ""
    version: str = "0.1.0"
    description: str = ""
    
    def on_load(self) -> None: ...
    def on_unload(self) -> None: ...
    def on_before_run(self, task: str) -> str: return task
    def on_after_run(self, result: str) -> str: return result

class PluginSystem:
    """插件加载/热插拔系统"""
    
    def __init__(self):
        self._plugins: Dict[str, PluginBase] = {}
        self._hooks: Dict[str, List[str]] = {}  # hook_name -> [plugin_names]
    
    def load(self, plugin_path: str) -> Optional[PluginBase]:
        """动态加载插件"""
        try:
            spec = importlib.util.spec_from_file_location(
                f"plugin_{hash(plugin_path)}", plugin_path
            )
            mod = importlib.util.module_from_spec(spec)
            spec.loader.exec_module(mod)
            
            # 查找PluginBase子类
            for attr_name in dir(mod):
                attr = getattr(mod, attr_name)
                if isinstance(attr, type) and issubclass(attr, PluginBase) and attr is not PluginBase:
                    plugin = attr()
                    self._plugins[plugin.name] = plugin
                    self._register_hooks(plugin)
                    plugin.on_load()
                    return plugin
        except Exception as e:
            print(f"Plugin load error ({plugin_path}): {e}")
        return None
    
    def _register_hooks(self, plugin: PluginBase) -> None:
        for hook_name in ["on_before_run", "on_after_run"]:
            method = getattr(plugin, hook_name, None)
            if method and method.__func__ is not getattr(PluginBase, hook_name):
                self._hooks.setdefault(hook_name, []).append(plugin.name)
    
    def unload(self, name: str) -> bool:
        plugin = self._plugins.pop(name, None)
        if plugin:
            plugin.on_unload()
            for hooks in self._hooks.values():
                if name in hooks:
                    hooks.remove(name)
            return True
        return False
    
    def execute_hook(self, hook_name: str, *args, **kwargs) -> Any:
        result = args[0] if args else kwargs.get("task", "")
        for plugin_name in self._hooks.get(hook_name, []):
            plugin = self._plugins.get(plugin_name)
            if plugin:
                hook_func = getattr(plugin, hook_name)
                result = hook_func(result)
        return result
    
    @property
    def loaded_count(self) -> int:
        return len(self._plugins)


