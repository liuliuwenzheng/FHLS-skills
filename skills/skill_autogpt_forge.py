def self_check() -> Dict[str, bool]:
    """运行8项自检"""
    checks = {
        "AgentConfig(配置模型)": False,
        "AgentTemplate(骨架生成)": False,
        "ComponentRegistry(组件注册)": False,
        "AgentBuilder(Builder模式)": False,
        "ConfigValidator(校验器)": False,
        "PluginSystem(插件系统)": False,
        "AgentProfile(描述符)": False,
        "connect_to_ga(GA嫁接)": False,
    }
    
    try:
        # 1. AgentConfig
        cfg = AgentConfig(name="test_agent")
        assert cfg.name == "test_agent"
        d = cfg.to_dict()
        assert "name" in d
        cfg2 = AgentConfig.from_dict(d)
        assert cfg2.name == cfg.name
        y = cfg.to_yaml()
        assert "test_agent" in y
        checks["AgentConfig(配置模型)"] = True
    except:
        pass
    
    try:
        # 2. AgentTemplate
        import tempfile
        with tempfile.TemporaryDirectory() as tmpdir:
            path = AgentTemplate.scaffold("my_agent", tmpdir, "Test Agent")
            assert os.path.exists(path)
            config_path = os.path.join(tmpdir, "my_agent_config.yaml")
            assert os.path.exists(config_path)
            test_path = os.path.join(tmpdir, "tests", "test_my_agent.py")
            assert os.path.exists(test_path)
        checks["AgentTemplate(骨架生成)"] = True
    except:
        pass
    
    try:
        # 3. ComponentRegistry
        reg = ComponentRegistry()
        reg.register(Component("test_tool", ComponentType.TOOL, lambda: "tool"))
        assert reg.count == 1
        comp = reg.get(ComponentType.TOOL, "test_tool")
        assert comp is not None
        assert comp.create() == "tool"
        result = reg.list_by_type(ComponentType.TOOL)
        assert len(result) == 1
        checks["ComponentRegistry(组件注册)"] = True
    except:
        pass
    
    try:
        # 4. AgentBuilder
        builder = AgentBuilder("test_builder")
        builder.with_llm("openai", "gpt-4").with_tool("web_search")
        build = builder.build()
        assert build["config"].name == "test_builder"
        assert "web_search" in build["config"].tools
        assert "config_hash" in build["profile"].to_dict()
        checks["AgentBuilder(Builder模式)"] = True
    except:
        pass
    
    try:
        # 5. ConfigValidator
        cfg = AgentConfig(name="", max_iterations=0)
        errors = ConfigValidator.validate(cfg)
        assert len(errors) >= 2
        assert any(e.field == "name" for e in errors)
        checks["ConfigValidator(校验器)"] = True
    except:
        pass
    
    try:
        # 6. PluginSystem
        ps = PluginSystem()
        assert ps.loaded_count == 0
        
        class TestPlugin(PluginBase):
            name = "test_plugin"
            version = "1.0"
            
            def on_before_run(self, task):
                return f"plugin({task})"
        
        tp = TestPlugin()
        ps._plugins[tp.name] = tp
        ps._register_hooks(tp)
        assert ps.loaded_count == 1
        result = ps.execute_hook("on_before_run", "hello")
        assert result == "plugin(hello)"
        checks["PluginSystem(插件系统)"] = True
    except:
        pass
    
    try:
        # 7. AgentProfile
        profile = AgentProfile(name="p1", version="1.0", 
                              capabilities=["tool1"], config_hash="abc123")
        d = profile.to_dict()
        assert d["name"] == "p1"
        j = profile.to_json()
        assert '"name": "p1"' in j
        checks["AgentProfile(描述符)"] = True
    except:
        pass
    
    try:
        # 8. connect_to_ga
        result = connect_to_ga()
        assert result["module"] == "skill_autogpt_forge"
        assert len(result["components"]) == 6
        checks["connect_to_ga(GA嫁接)"] = True
    except:
        pass
    
    return checks


