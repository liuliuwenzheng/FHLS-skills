# ③ ComponentRegistry -- 组件注册表
# ═══════════════════════════════════════════════════

class ComponentType(Enum):
    LLM = auto()
    TOOL = auto()
    MEMORY = auto()
    PLUGIN = auto()
    EVALUATOR = auto()
    PROMPT = auto()

@dataclass
class Component:
    """注册的组件"""
    name: str
    type: ComponentType
    factory: Callable
    version: str = "0.1.0"
    description: str = ""
    tags: List[str] = field(default_factory=list)
    dependencies: List[str] = field(default_factory=list)
    
    def create(self, **kwargs) -> Any:
        return self.factory(**kwargs)

class ComponentRegistry:
    """组件注册表 -- 类似Spring的IoC容器轻量版"""
    
    def __init__(self):
        self._components: Dict[str, Component] = {}
    
    def register(self, component: Component) -> None:
        key = f"{component.type.name.lower()}:{component.name}"
        self._components[key] = component
    
    def register_class(self, cls: type, type_: ComponentType, 
                       name: Optional[str] = None) -> Component:
        name = name or cls.__name__.lower()
        component = Component(
            name=name,
            type=type_,
            factory=cls,
            description=cls.__doc__ or ""
        )
        self.register(component)
        return component
    
    def get(self, type_: ComponentType, name: str) -> Optional[Component]:
        return self._components.get(f"{type_.name.lower()}:{name}")
    
    def create(self, type_: ComponentType, name: str, **kwargs) -> Any:
        comp = self.get(type_, name)
        if comp is None:
            raise KeyError(f"Component not found: {type_.name}/{name}")
        return comp.create(**kwargs)
    
    def list_by_type(self, type_: ComponentType) -> List[Component]:
        prefix = f"{type_.name.lower()}:"
        return [c for k, c in self._components.items() if k.startswith(prefix)]
    
    def search(self, query: str) -> List[Component]:
        q = query.lower()
        return [c for c in self._components.values() 
                if q in c.name.lower() or q in c.description.lower()]
    
    @property
    def count(self) -> int:
        return len(self._components)


