def connect_to_ga() -> Dict[str, Any]:
    """将AutoGPT Forge嫁接到GA生态"""
    
    installed = []
    
    # 注册到skill_registry
    try:
        from skill_registry import register_system_skill
        register_system_skill(
            "skill_autogpt_forge",
            ["forgebuilder", "agentscaffold", "pluginloader"],
            "AutoGPT Forge Agent开发框架骨髓内化"
        )
        installed.append("skill_registry")
    except ImportError:
        pass
    
    # 安装为GA插件(GStack)
    try:
        from skill_gstack import install_skill_as_plugin
        install_skill_as_plugin(
            "autogpt_forge",
            {
                "AgentConfig": AgentConfig,
                "AgentBuilder": AgentBuilder,
                "ComponentRegistry": ComponentRegistry,
                "PluginSystem": PluginSystem,
                "AgentTemplate": AgentTemplate,
            }
        )
        installed.append("gstack")
    except ImportError:
        pass
    
    return {
        "module": "skill_autogpt_forge",
        "components": ["AgentConfig", "AgentBuilder", "ComponentRegistry",
                       "PluginSystem", "AgentTemplate", "ConfigValidator"],
        "status": "mounted",
        "installed_to": installed or ["standalone"]
    }


